#include "sound.h"

//================================================
//コンストラクタ
//================================================
CSound::CSound()
{

}

//================================================
//デストラクタ
//================================================
CSound::~CSound()
{

}

//================================================
//サウンドファイル読み込み
//================================================
void CSound::LoadSound(char *name)
{
	Snd = LunaSound::CreateFromFile(name, true);
}

//================================================
//サウンド再生
//================================================
void CSound::PlaySound(float volume)
{
	LunaSound::SetVolume(Snd, volume);
	
	LunaSound::Play(Snd);
}

//================================================
//サウンド再生
//================================================
void CSound::PlaySoundA()
{
	if(!LunaSound::IsPlay(Snd) )
	{
		LunaSound::Play(Snd);
	}
}

//================================================
//サウンド再生
//================================================
void CSound::PlaySoundA(int loop, int sampoint)
{
	LunaSound::Play(Snd, loop, sampoint); 
	
}

//================================================
//サウンド停止
//================================================
void CSound::StopSound(void)
{
	LunaSound::Stop(Snd);
}

//================================================
//メモリ開放
//================================================
void CSound::Release(void)
{
	LunaSound::Release(Snd);
}